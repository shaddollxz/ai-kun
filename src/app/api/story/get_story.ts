export function getStory(storyType: string) {
  if (storyType === "儿童故事") {
    return "在一个大森林里，小鹿有一天到乌龟家里去做客。乌龟妈妈叫小乌龟去摘些青菜，来给小鹿吃。小乌龟高高兴兴地去了。";
  } else if(storyType === "历史故事") {
    return "退避三舍：春秋时期，晋国内乱，晋献公的儿子重耳逃到楚国。楚成王收留并款待他，他许诺如晋楚发生战争晋军将退避三舍（一舍为三十里）"
  }
  return "有一猎人带着他的猎狗从野外归来，恰巧碰见一位渔夫带着满满一筐鱼往家里走。猎人非常希望得到鱼，渔夫同样希望得到猎物带里的猎物。";
}
